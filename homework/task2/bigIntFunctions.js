export function getMultiBigInt(a, b) {
    return BigInt(a) * BigInt(b);
}

export function getDivisionBigInt(a, b) {
    return BigInt(a) / BigInt(b);
}

export function getAdditionBigInt(a, b) {
    return BigInt(a) + BigInt(b);
}

export function getSubtractionBigInt(a, b) {
    return BigInt(a) - BigInt(b);
}

